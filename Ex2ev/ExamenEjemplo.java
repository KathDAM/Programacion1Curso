public class ExamenEjemplo {

    //imprime vectores
    public static void imprimir(int[] v) {
        System.out.print("{ ");
        for (int i = 0; i < v.length; i++) {
            System.out.print(v[i] + " ");
            
        }
        System.out.println("}");
    }





























}
